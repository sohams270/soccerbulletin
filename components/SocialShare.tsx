const PLATFORMS = ["facebook", "whatsapp", "instagram", "reddit", "snapchat"] as const;
const LABELS: Record<string, string> = {
  facebook: "Facebook",
  whatsapp: "WhatsApp",
  instagram: "Instagram",
  reddit: "Reddit",
  snapchat: "Snapchat",
};

interface SocialShareProps {
  /** Link to share. Defaults to the site origin. */
  url?: string;
  /** Text used in share intents. Defaults to a generic site message. */
  title?: string;
  /** Heading shown above the icons. */
  label?: string;
  /** "bar" = full-width footer bar; "inline" = compact in-article row. */
  variant?: "bar" | "inline";
}

export default function SocialShare({
  url,
  title,
  label = "Share SoccerBulletin",
  variant = "bar",
}: SocialShareProps = {}) {
  const [copied, setCopied] = useState(false);

  const siteUrl = () =>
    url ?? (typeof window !== "undefined" ? window.location.origin : "");

  const copyLink = async (note = "Link copied!") => {
    const url = siteUrl();
    try {
      await navigator.clipboard.writeText(url);
    } catch {
      // Older browsers: fall back to a temporary textarea.
      const ta = document.createElement("textarea");
      ta.value = url;
      ta.style.position = "fixed";
      ta.style.opacity = "0";
      document.body.appendChild(ta);
      ta.select();
      try {
        document.execCommand("copy");
      } catch {
        /* ignore */
      }
      document.body.removeChild(ta);
    }
    setCopied(true);
    window.setTimeout(() => setCopied(false), 2000);
    return note;
  };

  const share = (platform: string) => {
    const url = siteUrl();
    const u = encodeURIComponent(url);
    const text = encodeURIComponent(
      title ?? "Check out SoccerBulletin — your front row seat to world football"
    );
    const intents: Record<string, string> = {
      facebook: `https://www.facebook.com/sharer/sharer.php?u=${u}`,
      whatsapp: `https://api.whatsapp.com/send?text=${text}%20${u}`,
      reddit: `https://www.reddit.com/submit?url=${u}&title=${text}`,
    };
    // Facebook / WhatsApp / Reddit have web share intents; also copy the link
    // so the user always has it on the clipboard to paste anywhere.
    if (intents[platform]) {
      window.open(intents[platform], "_blank", "noopener,noreferrer");
      void copyLink();
    } else {
      // Instagram & Snapchat have no web share URL — copy the link to share.
      void copyLink();
    }
  };

  return (
    <section
      className={`share-bar${variant === "inline" ? " share-inline" : ""}`}
      aria-label={label}
    >
      <span className="share-label">{label}</span>
      <div className="share-icons">
        {PLATFORMS.map((p) => (
          <button
            key={p}
            type="button"
            className={`share-btn ${p}`}
            aria-label={`Share on ${LABELS[p]}`}
            title={`Share on ${LABELS[p]}`}
            onClick={() => share(p)}
          >
            <svg viewBox="0 0 24 24" aria-hidden fill="currentColor">
              <path d={ICONS[p]} />
            </svg>
          </button>
        ))}
      </div>
      <span
        className={`share-toast${copied ? " show" : ""}`}
        role="status"
        aria-live="polite"
      >
        Link copied!
      </span>
    </section>
  );
}
